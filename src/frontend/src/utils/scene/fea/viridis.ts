/** Map a normalized scalar (clamped to [0, 1]) to an RGB triplet
 * with each channel in [0, 1]. ``out`` may be the destination
 * array's row to avoid allocations. */
export function viridis(t: number, out: Float32Array, offset = 0): void {
    const tt = Math.min(1, Math.max(0, isFinite(t) ? t : 0));
    const i = Math.min(255, Math.floor(tt * 256));
    out[offset + 0] = VIRIDIS_R[i] / 255;
    out[offset + 1] = VIRIDIS_G[i] / 255;
    out[offset + 2] = VIRIDIS_B[i] / 255;
}
